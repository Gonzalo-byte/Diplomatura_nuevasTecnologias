    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int bil1000 = 0;
        int bil500 = 0;
        int bil200 = 0;
        int bil100 = 0;
        int monto = 1;

        do {

            monto = Integer.parseInt(jTextField1.getText());

        } while (monto % 100 != 0);

        bil1000 = monto / 1000;
        bil500 = (monto % 1000) / 500;
        bil200 = ((monto % 1000) % 500) / 200;
        bil100 = (((monto % 1000) % 500) % 200) / 100;
        
        jTextField2.setText(""+bil1000);
        jTextField3.setText(""+bil500);
        jTextField4.setText(""+bil200);
        jTextField5.setText(""+bil100);
        


    }//GEN-LAST:event_jButton1ActionPerformed

